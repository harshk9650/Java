public class LoopDemo {
public static void main(String[] args) {
    
    for(int i=1;i<=3;i++){
        System.out.println("For iteration "+i);
    }

    int j=1;
    while(j<=3){
        System.out.println("While iteration "+j);
        j++;
    }
    
    int k=1;
    do{
        System.out.println("Hello World");
        k++;
    }
    
    while(k<=3);

    }
}

